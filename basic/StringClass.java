package com.rays.basic;

public class StringClass {
	public static void main (String [] arg) {
		
		String name = "Deepak Verma";
		
		System.out.println("name"+ name);
		System.out.println(name.length());
		System.out.println("5th char is" +name.charAt(6));
		System.out.println("first char of a"+ name.indexOf('a'));
		System.out.println("last index of a" +name.lastIndexOf('a'));
		System.out.println(name.replace('a','b'));
		System.out.println(name.startsWith("Deepak"));
		System.out.println(name.endsWith("Verma"));
		System.out.println(name.toUpperCase());
		System.out.println(name.toLowerCase());
		System.out.println(name.substring(0,6));
		
		
		
		
		
		
		
		
		
		
			
		}
	}
	



package com.rays.basic;

public class TaypeCasting {
	
	public static void main(String[] args) {
		
		int i =10;
		System.out.println("int ="+i);
		
		double d =(double)i;
		System.out.println("double ="+d);
		
		char c= 'B';
		
		System.out.println("char ="+c);
		
		int j =(int)c;
		
		System.out.println("int ="+j);
		
	}

}

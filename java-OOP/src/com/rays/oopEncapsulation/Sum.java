package com.rays.oopEncapsulation;

public class Sum {
	
	public void sum(int i,int j) {
		System.out.println("sum of two int number is :"+(i+j));
	}
	public void sum(int i ,int j,int k) {
		System.out.println("sum of three int no :"+(i+j+k));
	}
	public void sum(double i,double j) {
		System.out.println("sum of two double no :"+(i+j));
		
	}
	

}
